public class BangunRuang{

  private int panjang;
  private int lebar;
  private int tinggi;
  
  //void tidak mengembalikan nilai
  public void setPanjang (int p){
    panjang = p;
  }
  public void setLebar (int l){
    lebar = l;
  }
  public void setTinggi (int t){
    tinggi = t;
  }
   
  public int getPanjang(){
    return panjang;
  }
  public int getLebar(){
    return lebar;
  }
  public int getTinggi(){
    return tinggi;
  }
  
  
    
}